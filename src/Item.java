
public class Item {
    private int _count;
    private String _name;
    private double _cost;

    public void setCount(int count) {
        _count = count;
    }

    public void setName(String name) {
        _name = name;
    }

    public void setCost(double cost) {
        _cost = cost;
    }

    int getCount() {
        return _count;
    }

    String getName() {
        return _name;
    }


    double getCost() {
        return _cost;
    }

}
