
public class Date {
    private int _day;
    private int _month;
    private int _year;

    public void setDay(int day) {
        _day = day;
    }

    public void setMonth(int month) {
        _month = month;
    }

    public void setYear(int year) {
        _year = year;
    }

    public int getDay() {
        return _day;
    }

    public int getMonth() {
        return _month;
    }

    public int getYear() {
        return _year;
    }
}
